@BACKBONES.register_module
class I3D(nn.Module):

    def __init__(self, pretrained=None, bn_eval=True, bn_frozen=False, partial_bn=False, input_channel=4, phase='train'):
        super(I3D, self).__init__()
        self.phase = phase
        self.loss = None
        self.pretrained = pretrained
        self.bn_eval = bn_eval
        self.bn_frozen = bn_frozen
        self.partial_bn = partial_bn
        self.features = nn.Sequential(
            BasicConv3d(input_channel, 64, kernel_size=7, stride=(2,2,2), padding=3), # (64, 32, 112, 112)
            nn.MaxPool3d(kernel_size=(1,3,3), stride=(1,2,2), padding=(0,1,1)),  # (64, 32, 56, 56)
            BasicConv3d(64, 64, kernel_size=1, stride=1), # (64, 32, 56, 56)
            BasicConv3d(64, 192, kernel_size=3, stride=1, padding=1),  # (192, 32, 56, 56)
            nn.MaxPool3d(kernel_size=(1,3,3), stride=(1,2,2), padding=(0,1,1)),  # (192, 32, 28, 28)
            Mixed_3b(), # (256, 32, 28, 28)
            Mixed_3c(), # (480, 32, 28, 28)
            nn.MaxPool3d(kernel_size=(3, 3, 3), stride=(2, 2, 2), padding=(1, 1, 1)), # (480, 16, 14, 14)
            Mixed_4b(),# (512, 16, 14, 14)
            Mixed_4c(),# (512, 16, 14, 14)
            Mixed_4d(),# (512, 16, 14, 14)
            Mixed_4e(),# (528, 16, 14, 14)
            Mixed_4f(),# (832, 16, 14, 14)
            nn.MaxPool3d(kernel_size=(1, 2, 2), stride=(1, 2, 2), padding=(0, 0, 0)), # (832, 16, 7, 7)
            Mixed_5b(), # (832, 16, 7, 7)
            Mixed_5c(), # (1024, 16, 7, 7)
            #nn.AvgPool3d(kernel_size=(2, 7, 7), stride=1),# (1024, 8, 1, 1)
            #nn.AdaptiveAvgPool3d((16,1,1)) # (1024, 16, 1, 1)
        )
        self._enable_pbn = partial_bn

        if partial_bn:
            self.partialBN(True)

        self.is_inited = False

    def forward(self, x):
        #if self.is_inited is False:
        #    #self.init_weights()
        #    self.is_inited = True
        x = x.float()
        assert x.shape[2] == 32, '{}'.format(x.shape[2])
        feature = self.features(x)

        return feature

    def init_weights(self):
        #self.pretrained = 'modelzoo/inception_i3d_yi_imagenet_inflated.pth'
        if isinstance(self.pretrained, str):
            logger = logging.getLogger()
            for m in self.modules():
                if isinstance(m, nn.Conv3d):
                    kaiming_init(m)
                elif isinstance(m, nn.BatchNorm3d):
                    constant_init(m, 1)
            #load_checkpoint(self, self.pretrained, strict=False, logger=logger)
            load_dict = torch.load(self.pretrained)['state_dict']
            model_dict = self.state_dict()
            update_dict = {}
            for k,v in model_dict.items():
                update_dict[k] = v
            for key in load_dict:
                model_key = key.replace('backbone.', '')
                if 'features.0.conv.weight' in key:
                    model_dict[model_key][:,:3,:,:,:] = load_dict[key]
                    model_dict[model_key][:,3,:,:,:] = load_dict[key][:,0,:,:,:]
                    model_dict[model_key] = model_dict[model_key] * 3 / 4
                    print("loaded " + model_key)
                else:
                    if model_key in model_dict:
                        model_dict[model_key] = load_dict[key]
                        print("loaded " + model_key)
            self.load_state_dict(model_dict)


        elif self.pretrained is None:
            for m in self.modules():
                if isinstance(m, nn.Conv3d):
                    kaiming_init(m)
                elif isinstance(m, nn.BatchNorm3d):
                    constant_init(m, 1)

    def partialBN (self, enable):
        self._enable_pbn = enable

    def train(self, mode=True):
        """
        Override the default train() to freeze the BN parameters
        :return:
        """
        super(I3D, self).train(mode)

        if self.bn_eval:
            for m in self.modules():
                if isinstance(m, nn.BatchNorm3d):
                    m.eval()
                    if self.bn_frozen:
                        for params in m.parameters():
                            params.requires_grad = False

        count = 0
        if self._enable_pbn:
            print("Freezing BatchNorm3D except the first one.")
            for m in self.modules():
                if isinstance(m, nn.BatchNorm3d):
                    count += 1
                    if count >= 2: # _enable_pbn should always be true (2 if self._enable_pbn else 1):
                        m.eval()

                        # shutdown update in frozen mode
                        m.weight.requires_grad = False
                        m.bias.requires_grad = False
